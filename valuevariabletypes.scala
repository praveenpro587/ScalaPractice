object variable extends App{
  //if we defined a variable with val it is immutable
  val x:Int=10 
  println(x)
  
  val a:String="Praveen";
  println(a)
  
  val aBoolean:Boolean=false
  val aChar:Char='b'
  
  println(aBoolean,aChar)
  //Variables can be reassign
  var y=12
  println(y)
  y=13
  println(y)
}
